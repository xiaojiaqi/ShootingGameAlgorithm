void InitDirectedBullet16_3(
	float& vx, float& vy,  // 弾の速ﾃg
	float theta            // 発射角ﾃg
) {
	// 速さ3に､Iする速ﾃgのテーブ?
	static int v3[][2]={
		{ 3, 0}, { 3, 1}, { 2, 2}, { 1, 3},
		{ 0, 3}, {-1, 3}, {-2, 2}, {-3, 1},
		{-3, 0}, {-3,-1}, {-2,-2}, {-1,-3},
		{ 0,-3}, { 1,-3}, { 2,-2}, { 3,-1}
	};

	// 角ﾃgtheta（0?〜360?）を16方ﾅﾘ（0〜15）に変換する
	int dir=theta*16/360;

	// 弾の速ﾃg(vx,vy)をテーブ?から?める
	vx=v3[dir][0];
	vy=v3[dir][1];
}